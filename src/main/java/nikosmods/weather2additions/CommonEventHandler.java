package nikosmods.weather2additions;

import net.minecraft.server.level.ServerLevel;
import net.minecraft.world.entity.Entity;
import net.minecraft.world.entity.player.Player;
import net.minecraftforge.event.TickEvent;
import net.minecraftforge.event.server.ServerStartedEvent;
import net.minecraftforge.eventbus.api.SubscribeEvent;
import net.minecraftforge.fml.common.Mod;
import nikosmods.weather2additions.mapdata.SaveMapData;
import nikosmods.weather2additions.mapdata.ServerMapRendering;

@Mod.EventBusSubscriber(modid = Weather2Additions.MODID, bus = Mod.EventBusSubscriber.Bus.FORGE)
public class CommonEventHandler {
    private static int counter = 0;
    @SubscribeEvent

    public static void onTickEvent(TickEvent tickEvent) {
        if (counter == 0) {
            if (tickEvent instanceof TickEvent.LevelTickEvent levelTickEvent) {
                if (levelTickEvent.level instanceof ServerLevel serverLevel) {
                    for (Entity entity : serverLevel.getAllEntities()) {
                        if (entity instanceof Player player) {
                            tickPlayer(player, serverLevel);
                        }
                    }
                }
            }
            counter = Config.PLAYER_LOAD_TIMER.get();
        }
        counter--;
    }
    public static void tickPlayer(Player player, ServerLevel level) {
        ServerMapRendering.loadAroundPlayer(player, level);
    }
    @SubscribeEvent
    public static void onServerStart(ServerStartedEvent startedEvent) {
        SaveMapData.get();
    }

}

